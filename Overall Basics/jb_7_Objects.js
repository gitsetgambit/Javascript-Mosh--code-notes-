// person is a object here with attributes like
//  name and age
let person = {
  name: "mosh",
  age: 60,
};

/* how to change attributes of the object 
which is person in this case */

// dot notation
person.name = "john";

// bracket notaion
let selection = "name";
person[selection] = "Aditya";

// simple printing
console.log(person.name);
